# Operadores Lógicos em Python: TRUE or FALSE (maior, maior igual, menor, menor igual, igual, diferente)

print(8 > 4)
# true (8 é maior que 4)

print(4 > 8)
# false (4 não é maior que 8)

print(4 >= 4)
# true (4 é maior ou igual a 4)

print(5 <= 4)
# false (5 não é menor ou igual a 4)

print(8 == 8)
# true (8 é igual a 8)

print(10 != 11)
# true (10 é diferente de 11)
