# Operadores Aritméticos em Python: soma, subtração, multiplicação e divisão

print(6+4)
# retorna a soma = 10

print(6-4)
# retorna a subtração = 2

print(6*4)
# retorna a multiplicação =24

print(6/4)
# retorna a divisão completa (com decimais, se houver) = 1.5

print(6//4)
# retorna a divisão inteira (sem decimais) = 1
